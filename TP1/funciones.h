#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED

/** \brief suma dos enteros
 *
 * \param int primer operador a sumar
 * \param int segundo operador a sumar
 * \return int devuelve los valores sumados
 *
 */

int sumar(int,int);

/** \brief resta dos enteros
 *
 * \param int primer operador a restar
 * \param int segundo operador restante
 * \return int devuelve los valores restados
 *
 */

int resta(int,int);

/** \brief divide dos enteros
 *
 * \param int primer operador a dividir
 * \param int segundo operador divisor
 * \return int devuelve los valores divididos
 *
 */

int division(int,int);

/** \brief multiplicacion dos enteros
 *
 * \param int primer operador a multiplicar
 * \param int segundo operador a multiplicar
 * \return int devuelve los valores multiplicados
 *
 */

int multiplicacion(int,int);

/** \brief factorial de un entero
 *
 * \param int primer operador factorial
 * \return int devuelve valor factorial
 *
 */

int factorial(int);

/** \brief valida si es no es cero
 *
 * \param int primer operador a validar
 * \return int devuelve valor 0 o 1
 *
 */


int valcero(int);

#endif // FUNCIONES_H_INCLUDED
